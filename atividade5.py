
num1 = float(input("Digite um numero: "))
num2 = float(input("Digite um numero: "))
print ("Escolha a operação:")
opera = int(input("1- soma, 2-subtração, 3-multiplicação, 4-divisão: "))


if opera == 1:
    print( num1 + num2)

elif opera == 2:
    print(num1 - num2)

elif opera == 3:
    print(num1 * num2)

if opera == 4:
    print(num1 / num2)






